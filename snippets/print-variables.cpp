#include <iostream>

using namespace std;

int main()
{
    int a = 128;
    float b = 0.1;
    double c = 0.3;
    char d = 'A';
    string e = "Hello, World!";
    bool f = true;

    cout << "a is " << a << endl;
    cout << "b is " << b << endl;
    cout << "c is " << c << endl;
    cout << "d is " << d << endl;
    cout << "e is " << e << endl;
    cout << "f is " << f << endl;

    return 0;
}
